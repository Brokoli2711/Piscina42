#include <unistd.h>

void	ft_swap(int *a, int *b)
{
	int aux;

	aux = *a;

	*a = *b;
	*b = aux;
}

int	main(void)
{
	int a;
	int b;

	a = 47;
	b = 48;

	write(1, &a, 2);
	write(1, &b, 2);
	ft_swap(&a, &b);
	write(1, &a + '0', 2);
	write(1, &b + '0', 2);
	return (0);
}
